package ch.hevs.exception;

public class HousesellingException extends RuntimeException {

	public HousesellingException() {
		super();
	}

	public HousesellingException(String arg0) {
		super(arg0);
	}

	public HousesellingException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public HousesellingException(Throwable arg0) {
		super(arg0);
	}

}
